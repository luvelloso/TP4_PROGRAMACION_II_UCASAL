import numpy as np

# Dado el array
arr = np.array([1, 5, -2, 10, 2])

# a) Suma de los elementos
suma = np.sum(arr)

# b) Promedio de los elementos
promedio = np.mean(arr)

# c) Ordenar el array
arr_ordenado = np.sort(arr)

# Mostrar los resultados
print("Suma:", suma)
print("Promedio:", promedio)
print("Array ordenado:", arr_ordenado)
